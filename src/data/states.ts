export function getStateBySlug(slug: string): StateData | undefined {
  return states.find(s => s.slug === slug);
}

export function getAllStateSlugs(): string[] {
  return states.map(s => s.slug);
}

// Helper functions for filtering
export function getStrongCrosswalkStates(): StateData[] {
  return states.filter(s => s.crosswalkLaw === "strong");
}

export function getCriticalFatalityStates(): StateData[] {
  return states.filter(s => s.fatalityTrend === "Critical");
}

export function getContributoryNegligenceStates(): StateData[] {
  return states.filter(s => s.faultSystem.includes("Contributory"));
}
